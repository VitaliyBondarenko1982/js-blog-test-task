const showGoods = () => {
  // const result = await getGoods();
  // const news = result.response.results;

  const newsList = document.querySelector('#news');
  const pagination = document.querySelector('#pagination');
  const notesOnPage = 9;
  const countOfItems = Math.ceil(news.length / notesOnPage);

  const showPage = (function() {
    let active;

    return function(item) {
      if (active) {
        active.classList.remove('pagination__item--active');
      }

      active = item;

      item.classList.add('pagination__item--active');

      const pageNum = +item.innerHTML;
      const start = (pageNum - 1) * notesOnPage;
      const end = start + notesOnPage;
      const articles = news.slice(start, end);

      newsList.innerHTML = '';

      for (const article of articles) {
        const newsItem = document.createElement('li');

        newsItem.classList.add('news__item');
        newsList.appendChild(newsItem);

        const newsTop = document.createElement('div');

        newsTop.classList.add('news__top');
        newsItem.appendChild(newsTop);

        const newsBottom = document.createElement('div');

        newsBottom.classList.add('news__bottom');
        newsItem.appendChild(newsBottom);

        createBox(article.sectionName, newsBottom);
        createBox(article.webTitle, newsBottom);
        createBox(article.webUrl, newsBottom);
      }
    };
  }());

  const paginationItems = [];

  for (let i = 1; i <= countOfItems; i++) {
    const li = document.createElement('li');

    li.classList.add('pagination__item');
    li.innerHTML = i;
    pagination.appendChild(li);
    paginationItems.push(li);
  }

  showPage(paginationItems[0]);

  for (const paginationItem of paginationItems) {
    paginationItem.addEventListener('click', function() {
      showPage(this);
    });
  }

  function createBox(text, li) {
    const div = document.createElement('div');

    div.innerHTML = text;
    li.appendChild(div);
  }
};

showGoods();
